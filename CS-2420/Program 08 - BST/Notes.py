
# Trees
# A tree is balanced if all its nodes are balanced
# A node is balanced if its children subtree are within 1 depth
# Binary trees nodes have at most two children
# Leaf nodes have no children
# 2*log2N is best case depth if data is random
# Trees can be balanced, complete, full
# Full: everything has as many children as possible (2 for binary search tree)
# Complete: all levels are as full as they can be, the last descendants start left to right

